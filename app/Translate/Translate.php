    public function strUrlCHPU($str)
    {
        // переводим в транслит
        $str = $this->translateInEnglish( $str );
        // в нижний регистр
        $str = strtolower( $str );
        // заменям все ненужное нам на "-"
        $str = preg_replace( '~[^-a-z0-9_]+~u', '-', $str );
        // удаляем начальные и конечные '-'
        $str = trim( $str, "-" );
        return $str;
    }
